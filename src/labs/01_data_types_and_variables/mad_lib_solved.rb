class Lab
  def return_a_string
    # return a string with a name
    "Dr. Oo"
  end

  def return_an_integer
    # return an integer of an age
    287
  end

  def return_an_array
    # return an array with at least 3 adjectives as strings
    ["vivacious", "vigilant", "victorious"]
  end

  def return_a_hash
    # return a hash (key, value pair) of your favorite kind of thing
    # EX: {sport: basketball} or {food: artichoke}
    {language: "ruby"}
  end

  def return_true
    # write a conditional statement that will always return true
    2 + 2 == 4
    # notice the double equals sign? This is checking for equality, not setting a variable. We'll go over operators in another module
  end

  def return_false
    # write a conditional statement that will always return false
    2 + 2 == "banana"
  end
end
